if alien_color == 'green':
    print('\nCongrats you gained 5 points')
elif alien_color == 'yellow':
    print('\nWoow you gained 10 points')
elif alien_color == 'red':
    print('\nYeah you gained 15 points')
else:
    print('\nYou lost buddy')

